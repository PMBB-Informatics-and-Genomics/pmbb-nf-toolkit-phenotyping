#!/usr/bin/env python3
"""
pregnancy_tsv_to_event_mask.py

Convert a pregnancy ICD10 TSV (with TRIMESTER and ICD10 columns) into an
event_mask.yaml config file, grouping codes by trimester.

Usage:
    python bin/pregnancy_tsv_to_event_mask.py \
        --input pregnancy_codes.tsv \
        --output event_mask.yaml \
        [--source ICD10] \
        [--offset-before -270] \
        [--offset-after 90]

The input TSV must have at least these columns (tab-separated):
    TRIMESTER   ICD10

One YAML event is emitted per unique TRIMESTER value.
The --offset-before / --offset-after flags set the same offsets for all events;
edit the output YAML to give each event its own offsets.
"""

import argparse
import sys
from collections import defaultdict

import pandas as pd
import yaml


def load_tsv(path: str) -> pd.DataFrame:
    df = pd.read_csv(path, sep='\t', dtype=str)
    for col in ('TRIMESTER', 'ICD10'):
        if col not in df.columns:
            sys.exit(f"ERROR: required column '{col}' not found in {path}.\n"
                     f"Columns present: {list(df.columns)}")
    return df


def build_events(df: pd.DataFrame, source: str,
                 offset_before: int, offset_after: int) -> dict:
    grouped: dict = defaultdict(list)
    for _, row in df.iterrows():
        trimester = str(row['TRIMESTER']).strip()
        code = str(row['ICD10']).strip()
        if trimester and code and trimester != 'nan' and code != 'nan':
            grouped[trimester].append(code)

    events = {}
    for trimester, codes in sorted(grouped.items()):
        events[trimester.lower().replace(' ', '_')] = {
            'source': source,
            'codes': codes,
            'offset_before': offset_before,
            'offset_after': offset_after,
        }
    return events


def write_yaml(events: dict, output_path: str) -> None:
    doc = {'events': events}

    header = (
        "# Auto-generated by pregnancy_tsv_to_event_mask.py\n"
        "# Review offsets and source before use.\n"
        "# Bare codes (no dot) are prefix-matched: O83 → O83.0, O83.3, etc.\n"
        "# Dotted codes (e.g. O00.10) are exact matches.\n\n"
    )

    # Dump without aliases, keeping code lists readable
    yaml_str = yaml.dump(doc, default_flow_style=False, allow_unicode=True,
                         sort_keys=False)

    with open(output_path, 'w') as f:
        f.write(header)
        f.write(yaml_str)

    print(f"Wrote {len(events)} event(s) to {output_path}", file=sys.stderr)
    for name, cfg in events.items():
        print(f"  {name}: {len(cfg['codes'])} codes", file=sys.stderr)


def main():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument('--input',  required=True, help='Input TSV with TRIMESTER and ICD10 columns')
    p.add_argument('--output', required=True, help='Output event_mask YAML')
    p.add_argument('--source', default='ICD10',
                   help='Diagnostic phenotype name to use as source (default: ICD10)')
    p.add_argument('--offset-before', type=int, default=-270,
                   help='Days before event date to start window (default: -270)')
    p.add_argument('--offset-after',  type=int, default=90,
                   help='Days after event date to end window (default: 90)')
    args = p.parse_args()

    df = load_tsv(args.input)
    events = build_events(df, args.source, args.offset_before, args.offset_after)
    write_yaml(events, args.output)


if __name__ == '__main__':
    main()
